  let currentPreference = DEFAULT_PREFERENCE;
  let currentLanguage = DEFAULT_LANGUAGE;

  function normalizePreference(value) {
    const normalized = String(value || "")
      .trim()
      .toLowerCase();
    if (normalized === "system" || normalized === "en" || normalized === "es") {
      return normalized;
    }
    return DEFAULT_PREFERENCE;
  }

  function resolveLanguage(preference, navigatorLanguage) {
    const normalizedPreference = normalizePreference(preference);
    if (SUPPORTED_LANGUAGES.includes(normalizedPreference)) {
      return normalizedPreference;
    }

    const nav = String(navigatorLanguage || "")
      .trim()
      .toLowerCase();
    if (nav.startsWith("es")) {
      return "es";
    }
    if (nav.startsWith("en")) {
      return "en";
    }
    return DEFAULT_LANGUAGE;
  }

  function currentDictionary() {
    return dictionaries[currentLanguage] || dictionaries[DEFAULT_LANGUAGE];
  }

  function interpolate(template, params) {
    if (!params || typeof params !== "object") {
      return template;
    }
    return Object.entries(params).reduce((acc, [key, value]) => {
      return acc.replaceAll(`{${key}}`, String(value));
    }, template);
  }

  function t(key, params) {
    const dict = currentDictionary();
    const fallbackDict = dictionaries[DEFAULT_LANGUAGE] || {};
    const value = dict[key] ?? fallbackDict[key] ?? key;
    return interpolate(String(value), params);
  }

  function setLanguagePreference(preference, navigatorLanguage) {
    currentPreference = normalizePreference(preference);
    currentLanguage = resolveLanguage(currentPreference, navigatorLanguage || globalScope.navigator?.language);
    return currentLanguage;
  }

  function getLanguagePreference() {
    return currentPreference;
  }

  function getLanguage() {
    return currentLanguage;
  }

  function applyTranslations(root) {
    const scope = root && typeof root.querySelectorAll === "function" ? root : document;

    scope.querySelectorAll("[data-i18n]").forEach((node) => {
      const key = node.getAttribute("data-i18n");
      if (!key) {
        return;
      }
      node.textContent = t(key);
    });

    const attrs = [
      ["data-i18n-title", "title"],
      ["data-i18n-aria-label", "aria-label"],
      ["data-i18n-placeholder", "placeholder"],
    ];

    attrs.forEach(([dataAttr, attrName]) => {
      scope.querySelectorAll(`[${dataAttr}]`).forEach((node) => {
        const key = node.getAttribute(dataAttr);
        if (!key) {
          return;
        }
        node.setAttribute(attrName, t(key));
      });
    });
  }

  setLanguagePreference(DEFAULT_PREFERENCE, globalScope.navigator?.language);

  globalScope.WhisloAII18n = {
    dictionaries,
    resolveLanguage,
    setLanguagePreference,
    getLanguagePreference,
    getLanguage,
    t,
    applyTranslations,
  };
